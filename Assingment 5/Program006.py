'''
Q6. Predict the Output:

   x = None
   print(type(x))
   print(x == False)

   Explain the result

'''

x = None
print(type(x))
print(x == False)

'''
x is assigned None, which represents the absence of a value.
The type of None is NoneType.
None is not equal to False because False is a Boolean value, while None means no value.
'''